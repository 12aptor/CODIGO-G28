""" Integer (int) """
print(type(10))
print(type(0))

""" Float (float) """
print(type(10.5))

""" String (str) """
print(type("Hello, World!"))

""" Boolean (bool) """
print(type(True))

""" List (list) """
print(type([1, 2, 3]))

""" Tuple (tuple) """
print(type((1, 2, 3)))

""" Dictionary (dict) """
print(type({"key": "value"}))

""" Byte (bytes) """
print(type(b"Hello, World!"))

""" None (NoneType) """
print(type(None))
