""" Operadores aritméticos """
print(1 + 1) # Suma
print(1 - 1) # Resta
print(1 * 1) # Multiplicación
print(1 / 1) # División
print(1 // 1) # División entera
print(1 % 1) # Resto de la división
print(2 ** 3) # Potencia

""" Operadores de comparación """
print(1 == 1) # Igual
print(1 != 1) # Diferente
print(1 < 1) # Menor que
print(1 <= 1) # Menor o igual que
print(1 > 1) # Mayor que
print(1 >= 1) # Mayor o igual que

""" Operadores lógicos """
print(True and True) # And
print(True or False) # Or
print(not True) # Not

""" Operadores de asignación """
a = 1 # Asignación
a += 1 # Suma y asignación
a -= 1 # Resta y asignación
a *= 1 # Multiplicación y asignación
a /= 1 # División y asignación

""" Operadores de identidad """
print(1 is 1) # is (identidad)
print(1 is not 1) # is not (identidad)

""" Operadores de pertenencia """
print(1 in [1, 2, 3]) # in (pertenencia)
print("juanito" in "Hola juanito")